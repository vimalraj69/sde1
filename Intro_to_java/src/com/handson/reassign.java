package com.handson;
/*
 * Declare three integer variables named 'hundred', 'twoHundred', and 'threeHundred', and initialize them with the values 10, 2000, and -30000 respectively. Then, reassign them with the corresponding number names. Finally, print the values of all three
variables.

 */
import java.util.Scanner;
public class reassign {
	public static void main (String [] args) {
		short hundred=10,twoHundred=2000, threeHundred=-3000;
		hundred=100;
		twoHundred=200;
		threeHundred=-3000;
		System.out.println(hundred);
		System.out.println(twoHundred);
		System.out.println(threeHundred);
		
	}
}
